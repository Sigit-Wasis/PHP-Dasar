<?php
//Soal Operator Aritmatika
// Mendefinisikan variabel
$a = 15;
$b = 5;

// Menjumlahkan
$jumlah = $a + $b;

// Mengurangi
$kurang = $a - $b;

// Mengalikan
$kali = $a * $b;

// Membagi
$bagi = $a / $b;

// Sisa bagi
$sisabagi = $a % $b;

// Pangkat
$pangkat = $a ** $b;

// Menampilkan hasil nilai
echo "Jumlah: " . $jumlah . "\n";
echo "Selisih: " . $kurang . "\n";
echo "Hasil perkalian: " . $kali . "\n";
echo "Hasil bagi: " . $bagi . "\n";
echo "Sisa bagi: " . $sisabagi . "\n";
echo "Pangkat: " . $pangkat . "\n";

?>
